# 3.12 redefine a tuple

buffet = ('meat', 'chips', 'rice', 'potatoes', 'beans')

print("Our restaurant offers the following foods: \n")

for food in buffet:
    print(food)

buffet = ('meat', 'irish potatoes', 'rice', 'potatoes', 'beans')

print("\nOur new foods are the following: \n")

for food in buffet:
    print(food)

