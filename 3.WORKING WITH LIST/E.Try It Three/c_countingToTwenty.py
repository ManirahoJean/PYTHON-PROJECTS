# 3.3 Counting to twenty

countToTwenty = range(1, 21)
for value in countToTwenty:
    print(value)

print("\n")

new_countToTwenty = list(range(1, 21))
for new_value in new_countToTwenty:
    print(new_value)

print("\n")

numbers = []
for number in range(1, 21):
    numbers.append(number)
print(numbers)




