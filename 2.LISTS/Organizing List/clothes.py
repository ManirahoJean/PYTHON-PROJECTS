# Sorting a List Permanently with the sort() Method

clothes = ['shirt', ' coat', 't-shirt', 'jumper']
clothes.sort()
print(clothes)

# Sort this list in reverse alphabetical order

clothes.sort(reverse=True)
print(clothes)

cars = ['toyota', 'bmw', 'udi', 'subaru']
print(f"Here is the original list:\n{cars}")
print(f"Here is the sorted list: \n{sorted(cars)}")

# Printing a List in Reverse Order
cars.reverse()
print(cars)

# Finding the length of the list

print(len(cars))










