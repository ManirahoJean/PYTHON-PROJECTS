# A list is a collection of items in a particular order

animal = ['rabbit', 'cok', 'cat', 'dog']
print(animal)

# index positions starts from 0, not 1

print(animal[0])
print(animal[0].title())

# Accessing the last element in python

print(animal[-1])

message = f"My first animal was {animal[0].title()} !"
print(message)






